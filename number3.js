/*

3. Gunakan switch case untuk mengetahui nama hari berdasarkan angka.
    Hari minggu dimulai dari angka 1 dan seterusnya

*/

//SOLVE

let day = "minggu";

switch (day) {
  case "minggu":
    console.log("1");
    break;
  case "senin":
    console.log("2");
    break;
  case "selasa":
    console.log("3");
    break;
  case "rabu":
    console.log("4");
    break;
  case "kamis":
    console.log("5");
    break;
  case "jum'at":
    console.log("6");
    break;
  case "sabtu":
    console.log("7");
    break;
  default:
    console.log("data tidak sesuai");
}
